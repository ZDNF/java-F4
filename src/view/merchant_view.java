	//查看评论
	private void jButton1ActionPerformed(java.awt.event.ActionEvent evt)
			throws IOException, ClassNotFoundException {
		// TODO add your handling code here
		int pd = 0;
		int pd2 = 0;
		Client c = new Client();
		Object[][] dataObject = (Object[][]) c.allcommentlist();
		Client c2 = new Client();
		String[][] dataObject2 = c2.messagegame();
		for (int i = 0; i < dataObject.length; i++) {
			if (dataObject[i][3].equals(this.jTextField1.getText())) {
				view_commentlist();
				pd = 1;
			}
		}
		for (int j = 0; j < dataObject2.length; j++) {
			if (dataObject2[j][1].equals(this.jTextField1.getText())) {
				pd2 = 1;
			}
		}
		if (pd2 == 0) {
			JOptionPane.showMessageDialog(this, "不存在该游戏！");
		}
		if (pd2 == 1) {
			if (pd == 1) {
				JOptionPane.showMessageDialog(this, "查看评论成功！");
			}
			if (pd == 0) {
				JOptionPane.showMessageDialog(this, "该游戏还没有人评论！");
			}
		}
	}

