	//添加游戏
	private void jButton4ActionPerformed(java.awt.event.ActionEvent evt)
			throws IOException, ClassNotFoundException, SQLException {
		// TODO add your handling code here:
		int pd = 1;
		Client c = new Client();
		String[][] dataObject = c.messagegame();
		for (int i = 0; i < dataObject.length; i++) {
			if (dataObject[i][1].equals(this.jTextField3.getText())) {
				pd = 0;
			}
		}
		if (pd == 1) {
			Client c1 = new Client();
			if (this.jTextField3.getText() != null
					&& this.jTextField4.getText() != null
					&& this.jTextField5.getText() != null
					&& this.jTextField6.getText() != null) {
				String data = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss")
						.format(new Date());
				//System.out.println(m.getMername());
				c1.addgame(this.jTextField3.getText(), this.jTextField4
						.getText(), this.jTextField5.getText(),
						this.jTextField6.getText(), data, m.getMername());
				Client c2 = new Client();
				c2.reply(m.getMername(), "请点评该游戏", this.jTextField3.getText());
				JOptionPane.showMessageDialog(this, "添加游戏成功");
				game();
				game2();
				game3();
			}
		}
		if (pd == 0) {
			JOptionPane.showMessageDialog(this, "该游戏已存在，如有疑问请联系客服10086！");
		}
	}

